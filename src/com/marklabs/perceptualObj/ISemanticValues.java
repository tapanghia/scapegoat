package com.marklabs.perceptualObj;

import com.marklabs.brands.Brand;

public class ISemanticValues {

	private long id;
	private Brand brand;
	private double designValue;
	private double fragranceValue;
	private double persistenceValue;
	private double packagingValue;
	private double safetyValue;
	private double price;
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the brand
	 */
	public Brand getBrand() {
		return brand;
	}
	/**
	 * @param brand the brand to set
	 */
	public void setBrand(Brand brand) {
		this.brand = brand;
	}
	/**
	 * @return the designValue
	 */
	public double getDesignValue() {
		return designValue;
	}
	/**
	 * @param designValue the designValue to set
	 */
	public void setDesignValue(double designValue) {
		this.designValue = designValue;
	}
	/**
	 * @return the fragranceValue
	 */
	public double getFragranceValue() {
		return fragranceValue;
	}
	/**
	 * @param fragranceValue the fragranceValue to set
	 */
	public void setFragranceValue(double fragranceValue) {
		this.fragranceValue = fragranceValue;
	}
	/**
	 * @return the persistenceValue
	 */
	public double getPersistenceValue() {
		return persistenceValue;
	}
	/**
	 * @param persistenceValue the persistenceValue to set
	 */
	public void setPersistenceValue(double persistenceValue) {
		this.persistenceValue = persistenceValue;
	}
	/**
	 * @return the packagingValue
	 */
	public double getPackagingValue() {
		return packagingValue;
	}
	/**
	 * @param packagingValue the packagingValue to set
	 */
	public void setPackagingValue(double packagingValue) {
		this.packagingValue = packagingValue;
	}
	/**
	 * @return the safetyValue
	 */
	public double getSafetyValue() {
		return safetyValue;
	}
	/**
	 * @param safetyValue the safetyValue to set
	 */
	public void setSafetyValue(double safetyValue) {
		this.safetyValue = safetyValue;
	}
	/**
	 * @return the price
	 */
	public double getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
