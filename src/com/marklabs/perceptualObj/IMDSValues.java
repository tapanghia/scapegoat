package com.marklabs.perceptualObj;

import com.marklabs.brands.Brand;

public class IMDSValues {

	private long id;
	private Brand brand;
	private double economicValue;
	private double qualityValue;
	private double appealValue;
	
	
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the brand
	 */
	public Brand getBrand() {
		return brand;
	}
	/**
	 * @param brand the brand to set
	 */
	public void setBrand(Brand brand) {
		this.brand = brand;
	}
	/**
	 * @return the economicValue
	 */
	public double getEconomicValue() {
		return economicValue;
	}
	/**
	 * @param economicValue the economicValue to set
	 */
	public void setEconomicValue(double economicValue) {
		this.economicValue = economicValue;
	}
	/**
	 * @return the qualityValue
	 */
	public double getQualityValue() {
		return qualityValue;
	}
	/**
	 * @param qualityValue the qualityValue to set
	 */
	public void setQualityValue(double qualityValue) {
		this.qualityValue = qualityValue;
	}
	/**
	 * @return the appealValue
	 */
	public double getAppealValue() {
		return appealValue;
	}
	/**
	 * @param appealValue the appealValue to set
	 */
	public void setAppealValue(double appealValue) {
		this.appealValue = appealValue;
	}
	
	
}
