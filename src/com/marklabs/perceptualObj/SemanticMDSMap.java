package com.marklabs.perceptualObj;

public class SemanticMDSMap {

	private long id;
	private String MDSScale;
	private double DesignEq;
	private double fragranceEq;
	private double persistenceEq;
	private double packagingEq;
	private double safetyEq;
	private double priceEq;
	
	
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the mDSScale
	 */
	public String getMDSScale() {
		return MDSScale;
	}
	/**
	 * @param mDSScale the mDSScale to set
	 */
	public void setMDSScale(String mDSScale) {
		MDSScale = mDSScale;
	}
	/**
	 * @return the designEq
	 */
	public double getDesignEq() {
		return DesignEq;
	}
	/**
	 * @param designEq the designEq to set
	 */
	public void setDesignEq(double designEq) {
		DesignEq = designEq;
	}
	/**
	 * @return the fragranceEq
	 */
	public double getFragranceEq() {
		return fragranceEq;
	}
	/**
	 * @param fragranceEq the fragranceEq to set
	 */
	public void setFragranceEq(double fragranceEq) {
		this.fragranceEq = fragranceEq;
	}
	/**
	 * @return the persistenceEq
	 */
	public double getPersistenceEq() {
		return persistenceEq;
	}
	/**
	 * @param persistenceEq the persistenceEq to set
	 */
	public void setPersistenceEq(double persistenceEq) {
		this.persistenceEq = persistenceEq;
	}
	/**
	 * @return the packagingEq
	 */
	public double getPackagingEq() {
		return packagingEq;
	}
	/**
	 * @param packagingEq the packagingEq to set
	 */
	public void setPackagingEq(double packagingEq) {
		this.packagingEq = packagingEq;
	}
	/**
	 * @return the safetyEq
	 */
	public double getSafetyEq() {
		return safetyEq;
	}
	/**
	 * @param safetyEq the safetyEq to set
	 */
	public void setSafetyEq(double safetyEq) {
		this.safetyEq = safetyEq;
	}
	/**
	 * @return the priceEq
	 */
	public double getPriceEq() {
		return priceEq;
	}
	/**
	 * @param priceEq the priceEq to set
	 */
	public void setPriceEq(double priceEq) {
		this.priceEq = priceEq;
	}
	
	
	
}
