package com.marklabs.brands;

public class BrandMetadata {

	private long id;
	private String brandName;
	private String researchProjectName;
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the brandName
	 */
	public String getBrandName() {
		return brandName;
	}
	/**
	 * @param brandName the brandName to set
	 */
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	/**
	 * @return the researchProjectName
	 */
	public String getResearchProjectName() {
		return researchProjectName;
	}
	/**
	 * @param researchProjectName the researchProjectName to set
	 */
	public void setResearchProjectName(String researchProjectName) {
		this.researchProjectName = researchProjectName;
	}
	
	
	
}
