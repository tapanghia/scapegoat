package com.marklabs.utils;

public class ChannelConstants {

	private long id;
	private int period;
	private String constName;
	private double supermarket;
	private double generalStore;
	private double kiranaStore;
	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}
	/**
	 * @return the period
	 */
	public int getPeriod() {
		return period;
	}
	/**
	 * @param period the period to set
	 */
	public void setPeriod(int period) {
		this.period = period;
	}
	/**
	 * @return the constName
	 */
	public String getConstName() {
		return constName;
	}
	/**
	 * @param constName the constName to set
	 */
	public void setConstName(String constName) {
		this.constName = constName;
	}
	/**
	 * @return the supermarket
	 */
	public double getSupermarket() {
		return supermarket;
	}
	/**
	 * @param supermarket the supermarket to set
	 */
	public void setSupermarket(double supermarket) {
		this.supermarket = supermarket;
	}
	/**
	 * @return the generalStore
	 */
	public double getGeneralStore() {
		return generalStore;
	}
	/**
	 * @param generalStore the generalStore to set
	 */
	public void setGeneralStore(double generalStore) {
		this.generalStore = generalStore;
	}
	/**
	 * @return the kiranaStore
	 */
	public double getKiranaStore() {
		return kiranaStore;
	}
	/**
	 * @param kiranaStore the kiranaStore to set
	 */
	public void setKiranaStore(double kiranaStore) {
		this.kiranaStore = kiranaStore;
	}
	
	
	
	
}
